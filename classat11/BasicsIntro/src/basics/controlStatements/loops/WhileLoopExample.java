package basics.controlStatements.loops;

public class WhileLoopExample {
	public static void main(String[] args) {
		int i;
		i=1;												//initialization
		while(i<=5) {										//while(condition)
			System.out.println(i+". While loop working...");
			i++;											//iteration
		}
	}
	
	
	/*
	 1. Initialization
	 2. Condition
	 3. Iteration
	 
	 
	 while loop syntax
	 initialization
	 while(condition){
	 	block of code to be repeated
	 	iteration
	  	
	  }
	 
	 */
}
