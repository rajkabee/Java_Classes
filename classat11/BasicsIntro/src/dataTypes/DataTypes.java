package dataTypes;

public class DataTypes {
	public static void main(String[] args) {
		System.out.println("Hello World!");
		//integer
			byte b = 127;		//8bits
			short s = 32767;	//16bits
			int i = 2147483647;		//32bits
			long l = 9223372036854775807l;	//64bits
			
		//float
			float f = 1.234f;	//32bits
			double d = 1.2345;	//64bits
			
		//character
			char ch = 'A';
			
		//boolean
			boolean bool = true;
			
		//String
			String str = "string";
	}
}
